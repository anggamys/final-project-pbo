package com.mycompany.final_project_pbo.models;

/**
 *
 * @author workspace
 */
public class Dropdown {
    private Integer id;
    private String name;

    public Dropdown() {
        // Default constructor
    }

    public Dropdown(Integer id, String name) {
        this.id = id;
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
}
